package programPractice;

public class DecisionMakingStatement {

	public static void main(String[] args) {
		
		String fruit ="Apple";

		switch(fruit) {
		case "Orange":
		System.out.println("fruit Orange");
		break;
		case "Apple":
		System.out.println("fruit Apple");
		break;
		default:
		System.out.println("Not a fruit");
		}
	}

}
